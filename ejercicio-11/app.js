// Perímetro rectangulo: es la suma de sus cuatro lados

const valorAltura = prompt("Ingresa el valor de la altura de un rectángulo");
const valorAncho = prompt("Ingresa el valor del ancho de un rectángulo");

const perimetro = (valorAltura * 2)+(valorAncho * 2);

alert(`El perímetro del rectángulo es de ${perimetro}`);